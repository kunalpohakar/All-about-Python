# While Loop
i = 0
while i < 50:  # it means while this condition is true print or run this statement.
    print(i)
    # break     # break is a keyword in python and right now it's break the looping. 
                # and we'll also able to break out of the while loop as long as we're turn a condition into a false.
    i += 1
else:           # we can use else here also when the condition trun into false then the else statement will be executed.
    print('done with all work')
