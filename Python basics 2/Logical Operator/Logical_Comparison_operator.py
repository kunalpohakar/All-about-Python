# Logical Operator and Comparison Operators
"""
Comparison Operators are :
< : less than
> : greater than
<= : less than equal to
>= : greater than equal to
== : equal to equal to
!= : not equal to

Logical Operators are :
and
or
not

"""
# Comparison Operators
print(1 < 2)
print(1 > 2)
print(1 <= 0)
print(1 >= 0)
print(1 == 1)
print(1 != 1)

# Logical Operators
a = True
b = False

print(a and b)
print(a or b)
print(not a)
